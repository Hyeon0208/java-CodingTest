public class StringvalueOf {
    public static void main(String[] args) {
        int int1 = 1234;        
        int int2 = -1234;         
        
        String str1 = String.valueOf(int1);        
        String str2 = String.valueOf(int2);         
        
        System.out.println("String str1 : " + str1);        
        System.out.println("String str2 : " + str2);
	}
}
